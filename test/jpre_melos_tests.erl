-module(jpre_melos_tests).
-include_lib("eunit/include/eunit.hrl").

-compile([export_all]).

slow_test_() ->
    {timeout, 60,
        fun() ->
            accent_phrases_test_sub()
        end}.

accent_phrases_test_sub() ->
    lists:foreach(fun
        ({Text, Expected}) ->
            Result = accent_phrases_to_kana(Text),
            ?assertEqual(Expected, Result);
        (_) ->
            ok
    end, get_melos()).


accent_phrases_to_kana(Text) ->
    lists:foldl(fun
        (#{moras:=Moras,pause_mora:=PauseMora,accent:=AccentInt}, Base) ->
            MorasText = moras_to_kana(Moras),
            PauseMoraText = mora_to_kana(PauseMora),
            MorasKanaSize = size(MorasText) - 1,
            <<MorasKana:MorasKanaSize/binary, _/binary>> = MorasText,
            Accent = integer_to_binary(AccentInt),
            <<Base/binary, MorasKana/binary, PauseMoraText/binary, Accent/binary>>
    end, <<>>, jpre:accent_phrases(Text)).
moras_to_kana(Moras) ->
    lists:foldl(fun
        (Mora, Base) ->
            MoraKana = mora_to_kana(Mora),
            <<Base/binary, MoraKana/binary>>
    end, <<>>, Moras).
mora_to_kana(null) ->
    <<>>;
mora_to_kana(#{vowel:=<<"pau">>}) ->
    <<"_">>;
mora_to_kana(#{vowel:=Vowel, consonant:=null}) ->
    <<Vowel/binary, ",">>;
mora_to_kana(#{vowel:=Vowel, consonant:=Consonant}) ->
    <<Consonant/binary, ".", Vowel/binary, ",">>.


